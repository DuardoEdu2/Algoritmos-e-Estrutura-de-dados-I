#include <stdio.h>
#include "Modulo.h"

int soma(int num1, int num2){

    int result = num1 + num2;
    return result;

}

int subtracao(int num1, int num2){

    int result = num1 - num2;
    return result;

}

int multiplicacao(int num1, int num2){

    int result = num1 * num2;
    return result;

}

int divisao(int num1, int num2){

    float result = num1 + num2;
    return result;

}